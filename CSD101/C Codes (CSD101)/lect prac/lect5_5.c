#include<stdio.h>

int main()
{
int a = 5;
int b = 5;
printf("Positive Integer = %d\n", a);
printf("Negative Integer = %d\n", -a);
printf("Pre-Incrementing a = %d\n", ++a);
printf("Post-Incrementing b = %d\n", b++);
printf("%d\n", b);
printf("Pre-Decrementing a = %d\n", --a);
printf("Post-Decrementing b = %d\n", b--);
printf("%d\n", b);
}